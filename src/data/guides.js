// Helper functions
export function getAllCategories() {
  return guideCategories.map(({ slug, title, icon, description, topics }) => ({
    slug,
    title,
    icon,
    description,
    topicCount: topics.length,
  }));
}

export function getCategoryBySlug(slug) {
  return guideCategories.find((c) => c.slug === slug) || null;
}

export function getTopicBySlug(categorySlug, topicSlug) {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return null;
  const topic = category.topics.find((t) => t.slug === topicSlug);
  if (!topic) return null;
  return { ...topic, category };
}

export function getAdjacentTopics(categorySlug, topicSlug) {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return { prev: null, next: null };
  const idx = category.topics.findIndex((t) => t.slug === topicSlug);
  return {
    prev: idx > 0 ? category.topics[idx - 1] : null,
    next: idx < category.topics.length - 1 ? category.topics[idx + 1] : null,
  };
}
